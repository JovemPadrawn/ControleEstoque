public class Produto {
    private int id;
    private String nome;
    private int quantidade;
    private double preco;

    public Produto() {
    }

    public Produto(int id, String nome, int quantidade, double preco) {
        this.id = id;
        this.nome = nome;
        this.quantidade = quantidade;
        this.preco = preco;
    }

}
